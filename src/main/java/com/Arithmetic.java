package com;

public class Arithmetic{

    int add(int a,int b)
    {
        int c;
        c=a+b;
        return c;
    }
    int sub(int d, int e)
    {
        int f;
        f=d-e;
        return f;

    }
    int multiply(int g,int h)

    {
        int i;
        i=g*h;
        return i;

    }
    int divide(int j,int k)
    {
        int l;
        l=j+k;
        return l;
    }

}
